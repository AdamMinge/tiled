/*-----------------------------------------------------------------------------------------------------------*/
#include "frame.h"
/*-----------------------------------------------------------------------------------------------------------*/
Frame::Frame(int tileId, int duration) :
	mTileId(tileId),
	mDuration(duration)
{

}
/*-----------------------------------------------------------------------------------------------------------*/
bool Frame::operator==(const Frame& other) const
{
	if (mTileId != other.mTileId || mDuration != other.mDuration) return false;
	else return true;
}
/*-----------------------------------------------------------------------------------------------------------*/
bool Frame::operator!=(const Frame& other) const
{
	return !(*this == other);
}
/*-----------------------------------------------------------------------------------------------------------*/
void Frame::setTileId(int tileId)
{
	mTileId = tileId;
}
/*-----------------------------------------------------------------------------------------------------------*/
int Frame::tileId() const
{
	return mTileId;
}
/*-----------------------------------------------------------------------------------------------------------*/
void Frame::setDuration(int duration)
{
	mDuration = duration;
}
/*-----------------------------------------------------------------------------------------------------------*/
int Frame::duration() const
{
	return mDuration;
}
