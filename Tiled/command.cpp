#include "command.h"
/*-----------------------------------------------------------------------------------------------------------*/
Command::Command(Command* parent) :
	QUndoCommand(parent)
{

}
/*-----------------------------------------------------------------------------------------------------------*/
bool Command::canMergeWith(const Command* other) const
{
	return false;
}
/*-----------------------------------------------------------------------------------------------------------*/
void Command::mergeWith(const Command* other)
{
	Q_UNUSED(other);
}
/*-----------------------------------------------------------------------------------------------------------*/
bool Command::mergeWith(const QUndoCommand* other)
{
	auto command = static_cast<const Command*>(other);

	if (!canMergeWith(command)) return false;
	else
	{
		mergeWith(command);
		return true;
	}
}
